                                    <p>
                                        So let us try to define in brief what each of these terms mean.
                                    </p>
                                    
                                    <p>
                                        <b>Business Intelligence: </b> It is data driven decision making. It includes generation , aggregation , 
                                        analysis and visualization of data to inform and facilitate business management. Business Intelligence involves procedures and processes  that  help in data sharing , reporting and collection.
                                        Business Intelligence is at the middle of the data pyramid and above it comes business analysis.
                                    </p> 
                                    
                                    <p>
                                        <b>Analytics:</b> Analytics is the way in which you use the data to access the trend over time. By getting the processe data and reports you need to derive key performance indicators (parameters which lead to increase in the effectiveness of the business) . for example by analysing the data at a food store it was found that people who buy milk tend to buy eggs. So milk and breads were placed together which resulted in 
                                            convenience to people and increased sales. Analytics involves asking questions, generating insights from the data and using it to improve the business. 
                                            Business analytics can be considered to be top of the data pyramid.
                                    </p>			
                                    
                                    <p>
                                        <b>Big Data:</b> It is base of the data pyramid. Big data refers to the immense volume of data which is available from external and internal sources. Since the sources are diverse the data is generally raw and unstructured. So in order to make 
                                    data useful we need to clean and organise the data so that we can generate insights from it which can be used for analysis.
                                    </p>
                                    
                                               
                                    
                                    
