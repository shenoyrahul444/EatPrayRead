                                    <p>
                                        In order to achieve goals and planned results within a defined schedule and a budget, a manager uses a project management methodology.
                                        Regardless of which field or which trade, there are assortments of methodologies to help managers at every stage 
                                        of a project from the initiation to implementation to the closure. 
                                    </p>
                                    
                                    <h2>Waterfall Methodology (Traditional)</h2>
                                    
                                    <p>
                                        The waterfall model is a sequential design process, used in software development processes, in which progress is 
                                        seen as flowing steadily downwards (like a waterfall).
                                    </p> 
                                    
                                    <p>
                                        This methodology divides the project management process into 7 consecutive phases: <br>
                                        
                                            1>	Requirements specification<br>
                                            2>	 Design <br>
                                            3>	Construction (coding) <br>
                                            4>	Integration <br>
                                            5>	Validation (testing) <br>
                                            6>	Installation<br>
                                            7>	Maintenance<br>

                                    </p>			
                                    
                                    <p>
                                    The waterfall methodology is frequently used in the work domains where outputs are physical objects 
                                    as the whole process is a sequence and the process cannot move forward unless the previous stage is
                                    completed. The sequence of the stages needs to followed for the successful completion of the projects.
                                    Although it looks very clean and suits the requirements in domains with simple chain of work process,
                                    practically the waterfall methodology is not adaptable.  Due to its structured approach, scope changes are
                                    very slow, which makes it unsuitable for projects where the client is not sure about what he or she wants. 
                                    It is generally estimated that it takes about 20-40% of the total time allotted to the project is invested in
                                    the first 2 stages. Hence, when it comes to adaptability and speed, this traditional waterfall methodology lags behind.
                                        
                                    </p>
                                    
                                    <h2>Agile Methodology</h2>
                                    
                                    <p>
                                        To overcome the drawbacks of the waterfall approach, the agile methodology is used. It is an incremental and iterative project 
                                        management methodology in which the project lifecycle and the features of the end product are not clearly defined at the 
                                        starting of the project. Instead of that, the work is carried out through a few iterative phases called "sprints". Each
                                        sprint is composed of many tasks and has a small part of the end product as its output. Agile project management allows project
                                        managers to constantly gather feedback and refine the requirements between two iterations.
                                    </p>

                                    <p>
                                        
                                        When using this methodology, project responsibilities are divided among three roles : <br>
                                        1>	The product owner<br>
                                        2>	Scrum Master<br>
                                        3>	Team Members<br>
                                    
                                    The <b>product owner </b>sets the product goals, handles the tradeoff of schedule versus scope, adapting to changing project requirements and setting 
                                    priorities for product features. <br>
                                    
                                    The <b>scrum master </b>takes care of the prioritization of tasks and makes sure the impediments are removed from the work process.<br>
                                    The <b> team members </b>handle the tasks, managing the daily work details and preparing progress reports. They are also required to control the quality of the products which involves continuous testing. <br>
                                        </p>
                                        The agile development methodology is the best approach for projects with unpredictable changes in the scope, and are widely used in the areas of software development and designing. However, when the scope are limited and well defined, it is always better to make use of the traditional project management methodology which involves proper documentation, well defined scope, and more predictability of the scope.<br>

