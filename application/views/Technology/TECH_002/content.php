                                    <h2>Rapid Application Development (RAD)</h2>
                                    
                                    <p>
                                    Rapid application development is a project management methodology often used in software development where the main goal is to develop 
                                    applications faster and with high quality. It divides the project management process into four phases:    <br>
                                    1>	Requirements planning<br>
                                    2>	User design<br>
                                    3>	Rapid construction<br>
                                    4>	Transition<br>
                                    Rapid application development can improve quality of the deliverables and risk management. On the other hand, it is not suitable for large software projects, may lead to poor code quality and requires the client to be involved in project execution all the time.
                                    </p>
                                  
                                    <h2>Critical chain / path</h2>
                                    <p>
                                        As opposed to waterfall and agile project management, that focus more on schedules and tasks, the critical chain project management methodology 
                                        is geared more towards solving resource problems. Each project has a certain set of core elements, called a critical chain (sometimes referred as
                                        critical path), that establish a project’s minimum timeline. The critical chain methodology devotes adequate resources to this critical chain while 
                                        devoting enough resources to other tasks such that they can run concurrently, but still have enough of a buffer to reassign resources when needed. 
                                        This setup is ideal for resource-heavy teams, or for those who have enough flexibility in their team members’ respective skill sets.
                                    </p>
                                  
                                    <h2>PRiSM</h2>
                                    <p>
                                        Projects integrating Sustainable Methods (PRiSM) was developed by GPM Global as a means of creating a methodology that took environmental factors into
                                        account while being a repeatable, efficient process that could easily be incorporated into various large-scale projects. PRiSM is unique in that it is 
                                        one of the few project management methodologies that requires and rewards project managers with accreditation. PRiSM is used primarily for large-scale 
                                        real estate development or construction/infrastructure projects that may result in adverse environmental effects.
                                    </p>
                                    <h2>PRINCE2</h2>
                                    <p>
                                        PRINCE2 is a government-endorsed project management methodology, released and supported by the UK government in 1996. It is a very process-oriented methodology, dividing projects into multiple stages, each with their own plans and processes to follow. 
                                        Like PRiSM, PRINCE2 requires accreditation via exams. PRINCE2 is a complicated yet thorough methodology, but the project manager needs to determine whether or not it will scale properly with their specific requirements.<br>
                                        PRINCE stands for Projects in Controlled Environments. It is very commonly used project management method in all parts of the world and has proven to be highly effective in various respects.<br>
                                        The method also helps you to identify and thereafter assign roles to the different members of the team based on expertise. Over the years, there have been a number of positive case studies of projects that have used PRINCE2 project management methodology. 
                                        This method deals with the various aspects that need to be managed in any given project.


                                    </p>
