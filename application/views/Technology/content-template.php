                                    <p>Internet of things’ can termed as the 3rd Generation of Internet. We can say so, since the 1st Generation, around the 90s was PCs. 
                                        The 2nd Generation was born when Internet came in our hands i.e the mobile. It changed the pace of the internet. 
                                        Every person who may or may not have a PC, would have a mobile. This broadened the size of the internet to a great extent. 
                                    </p>    
                                    <blockquote>
                                        “At this juncture, we are looking at the next generation of internet where it’s now popping out smartphone and getting into things. We can ask ourselves now, ‘are we entering the post mobile era, just the way we entered the post PC era some time back?’ The thing could change, anytime. ” said Sandeep Sibal, Country Manager & VP- Business Development, Qualcomm India & South Asia.
                                    </blockquote> 
                                    <p>
                                     Now, as the focus shifts from the electronic devices such as PCs and mobiles to ‘things’, the entry of the concept of ‘Internet of Things’ will bring a key change in the industry. 
                                     Software and hardware will practically integrate to bring this to life.   
                                    </p>
                                    <blockquote>
                                        "As of now, we are at a very naive stage. Our market is still slow, in terms of user acceptance. There are things still to evolve and business models still to form. 
                                        Now that we have so many Ip addresses, it’s time we shifted to IPv6. Ipv4 will not be able to fulfil the need of an IP for every single thing. 
                                        IPv6 has been available some time now, but it needs to evolve further. There will be a time, when the same vendor will have a family of products. These products will be able to communicate with each other. 
                                        For example: If you have a family of products at home and you want to reduce the electricity bill. Each device will give the information to the consumer. 
                                        Thus he can then act accordingly." 
                                    
                                                                            <footer>
                                       Said 
                                            <cite title="Source Title">
                                                Awita Badonia, IT (CSM) Director India, Alcatel-Lucent.
                                            </cite>
                                        </footer>

                                    </blockquote>
                                    
                                   
                                    <p>
                                        A common consumer isn’t really interested in IoT. All he wants to know is how this concept can make his life simpler and better. 
                                        It essentially means, he wants to know if he can control all the electronics around him like ACs, Refrigirators and so on, through one touch. 
                                    </p>
                                    
                                    <blockquote>
                                        “As predicted, by 2020 IoT must enter the FairPlay. But I think by 2017, our products will be IoT ready.
                                        We look at it in a way that every house would have a samsung smartphone. Thus this technology will only make their lives better.” 
                                        <footer>
                                            Said
                                            <cite title="Source Title">
