                                    <p>
                                        From a technological point of view, we have already moved from a unidirectional communication to a bidirectional communication. 
                                        
                                    </p>			
                                
                                    <blockquote>
                                        “These devices have to be bidirectional. We don’t only need the messages from devices but also need to send back the corrections the moment any discrepancy is noted.” 
                                        <footer>
                                            Said
                                            <cite title="Source Title">
                                                C. Rama Krishna Reddy, Chief Executive Officer, Spoors Technology Solutions. 
                                            </cite>
                                        </footer>
                                    </blockquote>
                                    
                                    
                                    <p>
                                        The bandwidth rates have also come down which automatically makes bidirectional communication more sensible and realistic.
                                    </p>    								
                                    <p>
                                        The Internet of things is a concept which would be exciting to see being implemented. But the big question is, is the market ready for it? 
                                        Will people be ready to understand the value of it? The only possible way to make this a reality would be scaling it. 
                                        At a small scale, this concept will not only be hard to implement, it will also be unrealistically expensive. 
                                        As a good news, the smartphone user market and it’s penetration is rapidly increasing. This will solve the scalability hinderance in the coming years. 
                                        IoT is still at its very early stage and has a long way to go.
                                    </p>
                                    