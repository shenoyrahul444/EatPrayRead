                                    <p>Facebook now plans to help closures by showing ‘less’ of your ex on your feed</p>			
                                    <p>
                                        Admit it, it can be quite tough to get over your ex when you know exactly which restaurant they checked into, 
                                        or which movie they’re watching. Looks like Facebook knows that too. To help people in this matter, Facebook 
                                        is putting on trial its latest feature, which allows one’s news feed to be altered based on their relationship status. 
                                        Not just the existing status, but also the change from ‘in a relationship’ to ‘single’. For instance, 
                                        if you were in a relationship with someone, and are now single, you obviously broke up. Facebook will process this and 
                                        show less of your ex on your feed. These tools will of course, ask for the users permission before performing certain actions. 


                                    </p>
                                        			
                                
                                        <p>
                                        For Facebook, it looks like a good way to retain users who have recently gone through some relational changes in their lives. 
                                        In fact, it might also get such people on the platform because Facebook will be one of the few getaways for them. This tool is still under trials,
                                        and user acceptance of this is highly unpredictable.
                                        </p>    
                            
