									<p></p>
									
									<p>
										Eggs are extremely versatile as an ingredient, as they can be incorporated anywhere and everywhere.  They act as a thickening agent in custards, leavening agents in cakes, clarifying agents in soups, emulsifying agents in mayonnaise, binding agents in cutlets and as glazing agents. 
									</p>
									
