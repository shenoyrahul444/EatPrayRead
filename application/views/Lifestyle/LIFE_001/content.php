									<p>
										Egg whites when beaten well, become foamy, giving the recipe a light texture. This assists in leavening too, making the recipe look scrumptious and presentable. Of course, this means you need to get an egg white foam which is just about perfect and below are a few things you must keep in mind before you get beating!
									</p>

									<p>
1. <b>Type of beater</b><br>
Using a beater with thick blades/wires will produce a better foam compared to using a beater with thin wires as they do not divide the egg cells easily and incorporate more air. 
										
									</p>
						
									<p>
						2. <b>Beating time</b><br>
The beating time required depends upon the speed and type of beater. The lesser the speed, longer the beating period. Also, as the time increases the volume increases. But, one has to be careful not to over beat as it reduces the stability. 
				
									</p>
									
									<p>
									
3. <b>Type of container</b><br>
Bowls with small round bottom are preferred over bowls with large flat bottom as they allow the beater to pick up egg mass easily. The size of the bowl should be according to the volume of eggs to be beaten.
	
									</p>
							
									<p>
							
4.<b> Thick and thin egg whites</b><br>
Thick egg whites obtained from freshest of the eggs produce foam of high stability. On the other hand, stored eggs have thin egg whites which produce foam of much greater volume. Although, the use of thick egg whites gives a better volume to cakes and meringues and is extensively used. 
			
									</p>
									
									<p>
									
5. <b>Temperature</b><br>
Eggs are beaten best at room temperature. So, keep the eggs at room temperature prior to preparing them. 

	
									</p>
									
									<p>
										
6. <b>Fat</b><br>
Adding fat to egg whites has the potential to decrease the foam. Here’s the answer, in case you were wondering why egg whites and yolk are beaten separately. 

									</p>
									
									<p>
										7. <b>Water</b><br>
Water leads to the dilution of egg whites. 40% of water to the volume of egg white is recommended as it increases the foam however this causes a significant reduction in its stability. 


									</p>
	
	<p>
									8. <b>Sugar</b><br>
Addition of sugar is complicated. There are mainly two ways to add sugar:
<br>
&nbsp; i.	<b>After beating:</b><br> This gives good stability to the mix and volume too. Hence, this procedure is preferred when making cakes and other related desserts. 
<br>
&nbsp; ii.	<b>Before beating:</b><br> The time taken to produce the foam is much more though there is an increase in stability. But at the same time, the foam produce is quite less and hence this method is usually not preferred.

</p>

									
									<p>
								
9. <b>Salt</b><br> 
An absolute no-no, as salt not only reduces the volume of the foam, but also reduces its stability. In addition, the product does not become softer or lighter in texture as desired. 

		
									</p>
									
									<p>
									
10. <b>Acid</b><br>
Acid here, refers to cream of tartar, acetic acid or citric acid. They make the foam very stable and also a little resistant to heat, but then you will need to beat for some more time. However, you could add the acid at the initial stage of the beating period.
	
									</p>
									
									<p>				
So, make sure you invest in a quality egg beater and use the right type of bowls to get the lightest egg white foam and bake the best of cakes, soufflés and omelets! 
